package com.cfu666.cs.my.bean;

public class FundManagerResBean {
	private String fundManagerId;
	private String avatar;
	private String name;
	private Integer gender;
	private String orgBriefName;
	private String mobile;
	private String summary;

	public String getFundManagerId() {
		return fundManagerId;
	}

	public void setFundManagerId(String fundManagerId) {
		this.fundManagerId = fundManagerId;
	}

	public String getAvatar() {
		return avatar;
	}

	public void setAvatar(String avatar) {
		this.avatar = avatar;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Integer getGender() {
		return gender;
	}

	public void setGender(Integer gender) {
		this.gender = gender;
	}

	public String getOrgBriefName() {
		return orgBriefName;
	}

	public void setOrgBriefName(String orgBriefName) {
		this.orgBriefName = orgBriefName;
	}

	public String getMobile() {
		return mobile;
	}

	public void setMobile(String mobile) {
		this.mobile = mobile;
	}

	public String getSummary() {
		return summary;
	}

	public void setSummary(String summary) {
		this.summary = summary;
	}

}
